#include "shell.h"

/**
 * get_enviro - returns the string array copy of our environ
 * @info: Structure with arguments to maintains constant function prototype.
 * Return: 0
 */
char **get_enviro(info_t *info)
{
	if (!info->environ || info->env_changed)
	{
		info->environ = list_to_strings(info->env);
		info->env_changed = 0;
	}

	return (info->environ);
}

/**
 * _unset_enviro - Remove an environment variable
 * @info: Structure of arguments that maintains constant function prototype
 *  Return: 1 on delete, else return 0
 * @var: string environment varible property
 */
int _unset_enviro(info_t *info, char *var)
{
	list_t *node = info->env;
	size_t i = 0;
	char *p;

	if (!node || !var)
		return (0);

	while (node)
	{
		p = starts_with(node->str, var);
		if (p && *p == '=')
		{
			info->env_changed = delete_node_at_index(&(info->env), i);
			i = 0;
			node = info->env;
			continue;
		}
		node = node->next;
		i++;
	}
	return (info->env_changed);
}

/**
 * _set_enviro - initiate a new environment variable,
 *             or change an existing one
 * @info: Structure of arguments that maintains constant function prototype
 * @var: string environment varible property
 * @value: the string env var value
 *  Return: 0
 */
int _set_enviro(info_t *info, char *var, char *value)
{
	char *buf = NULL;
	list_t *node;
	char *p;

	if (!var || !value)
		return (0);

	buf = malloc(_strlen(var) + _strlen(value) + 2);
	if (!buf)
		return (1);
	_strcpy(buf, var);
	_strcat(buf, "=");
	_strcat(buf, value);
	node = info->env;
	while (node)
	{
		p = starts_with(node->str, var);
		if (p && *p == '=')
		{
			free(node->str);
			node->str = buf;
			info->env_changed = 1;
			return (0);
		}
		node = node->next;
	}
	add_node_end(&(info->env), buf, 0);
	free(buf);
	info->env_changed = 1;
	return (0);
}
